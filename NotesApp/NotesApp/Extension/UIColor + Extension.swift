import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var appGreenColor: UIColor {
        return UIColor(red: 135/255, green: 203/255, blue: 185/255, alpha: 1.0)
    }
    
    @nonobjc class var appBackground: UIColor {
        return UIColor(red: 237/255, green: 237/255, blue: 237/255, alpha: 1.0)
    }
    
    @nonobjc class var appRedColor: UIColor {
        return UIColor(red: 226/255, green: 24/255, blue: 24/255, alpha: 1.0)
    }
}
